#include "libft.h"

void	*ft_calloc(size_t num, size_t size)
{
	unsigned char	*dst;
	unsigned int	total;
	unsigned int	i;

	total = num * size;
	dst = malloc (total);
	if (!dst)
		return (NULL);
	i = 0;
	while (total--)
	{
		dst[i] = 0;
		i++;
	}
	return ((void *)dst);
}
